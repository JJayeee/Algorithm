def is_good(text):
    cnt = 0
    for t in text:
        if t == '(':
            cnt += 1
        else:
            cnt -= 1
        if cnt < 0:
            return False
    return cnt == 0


def solution(text):
    if not text: return text

    if is_good(text):
        answer = text
    else:
        cnt_1 = 0
        cnt_2 = 0
        u = text
        v = ''
        for i in range(len(text)):
            if text[i] == '(':
                cnt_1 += 1
            else:
                cnt_2 += 1
            if cnt_1 == cnt_2:
                u = text[:i+1]
                v = solution(text[i+1:])
                break

        if is_good(u):
            answer = u + v
        else:
            new_u = ''
            for i in range(1, len(u)-1):
                new_u += '(' if u[i] == ')' else ')'

            answer = '(' + v + ')' + new_u

    return answer


